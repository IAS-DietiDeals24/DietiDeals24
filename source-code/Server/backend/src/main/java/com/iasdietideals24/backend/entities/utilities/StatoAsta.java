package com.iasdietideals24.backend.entities.utilities;

/**
 * L'enumeration "StatoAsta" rappresenta i diversi stati che può assumere l'asta.
 * I possibili stati sono: ACTIVE, CLOSED.
 */

public enum StatoAsta {
    /**
     * Indica che l'asta è in corso
     */
    ACTIVE,

    /**
     * Indica che l'asta è stata chiusa
     */
    CLOSED
}
